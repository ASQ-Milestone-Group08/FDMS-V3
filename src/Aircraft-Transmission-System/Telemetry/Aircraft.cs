namespace AircraftTransmissionSystem.Telemetry
{
    /// <summary>
    /// Enum Name: Aircraft
    /// Description: Represents the available aircraft for telemetry transmission.
    ///              Each aircraft has a corresponding telemetry data file.
    /// Values:
    ///   - C_FGAX: Aircraft with tail number C-FGAX
    ///   - C_GEFC: Aircraft with tail number C-GEFC
    ///   - C_QWWT: Aircraft with tail number C-QWWT
    /// </summary>
    public enum Aircraft
    {
        /// <summary>
        /// Aircraft with tail number C-FGAX
        /// </summary>
        C_FGAX,

        /// <summary>
        /// Aircraft with tail number C-GEFC
        /// </summary>
        C_GEFC,

        /// <summary>
        /// Aircraft with tail number C-QWWT
        /// </summary>
        C_QWWT
    }
}
